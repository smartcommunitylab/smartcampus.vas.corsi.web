package eu.trentorise.smartcampus.corsi.model;

import java.util.Collection;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.JoinTable;
import javax.persistence.ManyToMany;
import javax.persistence.OneToMany;


@Entity
public class Dipartimento extends BasicEntity {

	private static final long serialVersionUID = 8681710690984301605L;

	@Id
	@GeneratedValue
	private long id;

	@Column(name = "NOME")
	private String nome;

	@OneToMany(targetEntity=CorsoLaurea.class, mappedBy="dipartimento")
	private Collection<CorsoLaurea> corsi;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public Collection<CorsoLaurea> getCorsi() {
		return corsi;
	}

	public void setCorsi(Collection<CorsoLaurea> corsi) {
		this.corsi = corsi;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
	
	
	
}
