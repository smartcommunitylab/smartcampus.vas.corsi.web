package eu.trentorise.smartcampus.corsi.model;

public class UtenteCorsi {
	// id dell'utente
	private int id;

	// nome dell'utente
	private String nome;

	public UtenteCorsi() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

}
