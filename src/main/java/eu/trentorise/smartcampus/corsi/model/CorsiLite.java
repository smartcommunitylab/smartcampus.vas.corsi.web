package eu.trentorise.smartcampus.corsi.model;

public class CorsiLite {
	// id del corso
	private int id;

	// nome del corso
	private String name;

	public CorsiLite() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
