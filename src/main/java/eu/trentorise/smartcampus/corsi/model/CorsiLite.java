package eu.trentorise.smartcampus.corsi.model;

public class CorsiLite
{
	//id del corso
	private int id;
	
	//nome del corso
	private String nome;
	
	
	public CorsiLite()
	{
	}

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
